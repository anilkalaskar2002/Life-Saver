    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        
          try{
           String fname,lname,address,mobileno,age,blood_group;
           
           fname = txtfname.getText();
           lname = txtlname.getText();
           address = txtaddress.getText();
           mobileno= txtmobile.getText();
           age = txtage.getText();
           blood_group=txtblood.getItemAt(0);
           
           pst=conn.prepareStatement("insert into bodydonar(fname,lname,address,mobileno,age,blood_group)values(?,?,?,?,?,?)");
           
           pst.setString(1, fname);
           pst.setString(2, lname);
           pst.setString(3, address);
           pst.setString(4, mobileno);
           pst.setString(5, age);
           pst.setString(6, blood_group);
           
           pst.executeUpdate();
           JFrame f=new JFrame();
           JOptionPane.showConfirmDialog(f, "Record Saved");
//           Table_update();
           
           txtfname.setText("");
           txtlname.setText("");
           txtmobile.setText("");
           txtaddress.setText("");
           txtage.setText("");
           txtfname.requestFocus();
           
           
           } catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

